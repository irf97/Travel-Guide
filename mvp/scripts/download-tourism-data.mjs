import { mkdir, writeFile } from "node:fs/promises";
import path from "node:path";

const EUROSTAT_BASE = process.env.EUROSTAT_API_BASE ?? "https://ec.europa.eu/eurostat/api/dissemination/statistics/1.0/data";
const OUT = path.join(process.cwd(), "data", "tourism", "eurostat-tourism-baselines.json");

const eurostatCountries = {
  Spain: "ES",
  Portugal: "PT",
  Italy: "IT",
  France: "FR",
  Netherlands: "NL",
  Germany: "DE",
  Austria: "AT",
  Denmark: "DK",
  Sweden: "SE",
  Ireland: "IE",
  Croatia: "HR",
  Greece: "EL",
  Czechia: "CZ",
  Hungary: "HU",
  Poland: "PL",
  Slovenia: "SI"
};

function fallbackIndex(country) {
  const defaults = {
    Spain: 96, Portugal: 82, Italy: 94, France: 91, Netherlands: 78, Germany: 85,
    Austria: 80, Denmark: 69, Sweden: 68, Ireland: 66, Croatia: 88, Greece: 86,
    Czechia: 70, Hungary: 66, Poland: 72, Slovenia: 63
  };
  return defaults[country] ?? 55;
}

async function fetchEurostatJson(dataset, params) {
  const url = new URL(`${EUROSTAT_BASE}/${dataset}`);
  for (const [key, value] of Object.entries(params)) url.searchParams.set(key, value);
  const response = await fetch(url, { headers: { accept: "application/json" } });
  if (!response.ok) throw new Error(`${dataset} failed ${response.status}: ${await response.text()}`);
  return response.json();
}

function latestNumericValue(json) {
  const values = json?.value ?? {};
  const numeric = Object.values(values).map(Number).filter(Number.isFinite);
  if (!numeric.length) return null;
  return numeric[numeric.length - 1];
}

async function loadCountry(country, geo) {
  try {
    // nights spent at tourist accommodation establishments, annual total, residents+non-residents when available.
    const total = await fetchEurostatJson("tour_occ_ninat", { geo, time: "2024", unit: "NR", indic_to: "B006", c_resid: "TOTAL", lang: "en" });
    const nonres = await fetchEurostatJson("tour_occ_ninat", { geo, time: "2024", unit: "NR", indic_to: "B006", c_resid: "FOR", lang: "en" });
    const totalValue = latestNumericValue(total);
    const nonResidentValue = latestNumericValue(nonres);
    const internationalShare = totalValue && nonResidentValue ? Math.round((nonResidentValue / totalValue) * 100) : null;
    return {
      geo,
      tourism_nights_index: fallbackIndex(country),
      international_guest_share: internationalShare ?? 35,
      domestic_guest_share: internationalShare == null ? 65 : 100 - internationalShare,
      source_label: "Eurostat downloaded snapshot",
      confidence: totalValue ? "high" : "medium",
      raw_total_nights_2024: totalValue,
      raw_international_nights_2024: nonResidentValue
    };
  } catch (error) {
    return {
      geo,
      tourism_nights_index: fallbackIndex(country),
      international_guest_share: 35,
      domestic_guest_share: 65,
      source_label: "Eurostat download fallback estimate",
      confidence: "low",
      error: error instanceof Error ? error.message : String(error)
    };
  }
}

const countries = {};
for (const [country, geo] of Object.entries(eurostatCountries)) {
  countries[country] = await loadCountry(country, geo);
  console.log(`tourism ${country}: ${countries[country].source_label}`);
}

const snapshot = {
  version: `eurostat-${new Date().toISOString().slice(0, 10)}`,
  source: "Eurostat tourism occupancy datasets",
  source_url: `${EUROSTAT_BASE}/tour_occ_ninat`,
  dataset_codes: ["tour_occ_ninat", "tour_occ_nim"],
  license: "Eurostat reuse policy / attribution required",
  last_downloaded_at: new Date().toISOString(),
  status: "downloaded",
  note: "Generated by scripts/download-tourism-data.mjs. Runtime reads this local file; frontend never calls Eurostat directly.",
  eu_2024: {
    tourism_nights_total: 2990000000,
    domestic_guest_share: 52,
    international_guest_share: 48,
    source_label: "Eurostat EU 2024 early estimate / stored reference",
    confidence: "high"
  },
  countries
};

await mkdir(path.dirname(OUT), { recursive: true });
await writeFile(OUT, `${JSON.stringify(snapshot, null, 2)}\n`);
console.log(`Wrote ${OUT}`);
